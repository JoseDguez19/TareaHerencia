public class Cuadrilatero {
    
    private int x1, y1, x2, y2, x3, y3, x4, y4;
    
    public Cuadrilatero(int x1, int y1, int x2, int y2, int x3, int y3, int x4, int y4){
    }
    
    public double calcularArea(){
    
        return 0.5*x1*x2*y1*y2*x3*y3*x4*y4;
    }


}
